    private void uye_kitaplari(String no) {// NO ID BİLGİSİDİR
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        dtm.setRowCount(0);
        String Sql = "SELECT * FROM UYELER U,KITAP_ALMA KA WHERE KA.UYE_ID=U.ID AND  U.ID='" + no + "'   ORDER BY KA.IDKITAP_ALMA DESC";
        System.out.println(Sql);
        uye_kitaplar = new LinkedList();
        //System.out.println(jTable1.getColumnName(4));
        try {
            st = conn.createStatement();
            ResultSet rs = st.executeQuery(Sql);
            int i = 0;
            jLabel40.setText("0");
            while (rs.next()) {
                JButton jb = new JButton();
                jb.setText("Sil");
                jLabel40.setText(String.valueOf(rs.getRow()));
                Boolean deger = false;
                if (rs.getString("AKTIFLIK").equals("AKTIF")) {
                    deger = true;
                }
                Object satır[] = {rs.getString("kitap_no"), StringUtils.capitalize(rs.getString("kitap_adi")), rs.getString("verilis_tarihi"), rs.getString("getirme_tarihi"), rs.getString("uye_no"), deger, null, rs.getString("numeratör")};
                //uttonRender br=new ButtonRender();
                uye_kitaplar.add(rs.getInt("IDKITAP_ALMA"));
                //jTable1.getColumnModel().getColumn(4).setCellRenderer((TableCellRenderer) new ButtonRender().btt);
                i++;
                System.out.println(Arrays.toString(satır));
                dtm.addRow(satır);
            }

            jTable1.setModel(dtm);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "veritabanı hatası" + ex.toString());
        }

    }
